// define add function
var add = (a,b) => a + b;

// subtract function
var subtract = (a,b) => a - b;

// multiply function
var multiply = (a,b) => a * b;

// divide function
var divide = (a,b) => a / b;

// post increment function
var inc = (n) => { n++; return n };

// post decrement function
var dec = (n) => { n--; return n };

// parseInt function
var makeInt = (n) => parseInt(n, 10);

// parseFloat function
var preserveDecimal = (n) => parseFloat(n);
